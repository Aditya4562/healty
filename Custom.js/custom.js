function calculateBMI() {
  let height = document.getElementById("height").value;
  let weight = document.getElementById("weight").value;
  let result = document.getElementById("bmiResult");

  if (height === "" || weight === "") {
    result.innerHTML = "Please enter height and weight";
    result.style.color = "red";
    return;
  }

  height = height / 100;
  let bmi = (weight / (height * height)).toFixed(1);

  if (bmi < 18.5) {
    result.innerHTML = `BMI: ${bmi} (Underweight) – Eat calorie-rich & protein foods`;
    result.style.color = "#ff9800";
  } 
  else if (bmi < 24.9) {
    result.innerHTML = `BMI: ${bmi} (Normal) – Maintain balanced diet & exercise`;
    result.style.color = "#4caf50";
  } 
  else {
    result.innerHTML = `BMI: ${bmi} (Overweight) – Reduce sugar, exercise daily`;
    result.style.color = "#f44336";
  }
}
